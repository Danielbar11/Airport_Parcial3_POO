    private void generateMonths() {
        for (int i = 1; i < 13; i++) {
            monthBirthUp.addItem("" + i);
            monthDeparture.addItem("" + i);
            monthBirthReg.addItem("" + i);
        }
    }

    private void generateDays() {
        for (int i = 1; i < 32; i++) {
            dayBirthUp.addItem("" + i);
            dayDeparture.addItem("" + i);
            dayBirthReg.addItem("" + i);
        }
    }

