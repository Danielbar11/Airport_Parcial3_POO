        String arrivalLocationId = cBoxLocation2.getItemAt(cBoxLocation2.getSelectedIndex());
        String scaleLocationId = cBoxLocation3.getItemAt(cBoxLocation3.getSelectedIndex());
        String year = txtYearFlightReg.getText();
